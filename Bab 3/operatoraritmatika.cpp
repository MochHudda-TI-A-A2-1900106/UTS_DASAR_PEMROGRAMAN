#include <iostream>
using namespace std;

int main()
{
    int x = 81, y = 26;
    cout << x << "+" << y << " = "<< x+y << endl;
    cout << x << "-" << y << " = "<< x-y << endl;
    cout << x << "x" << y << " = "<< x*y << endl;
    cout << x << "/" << y << " = "<< x/y << endl;
    cout << x << "%" << y << " = "<< x%y << endl;
    cout <<"-" << x << " = "<< -x << endl;

    return 0;
}